namespace Tanks
{
	/// <summary>
	/// Структура определения интервала с плавающей точкой
	/// </summary>
	[System.Serializable]
	public struct Interval
	{
		/// <summary>
		/// Нижняя граница интервала
		/// </summary>
		public float Min;
		/// <summary>
		/// Верхняя граница интервала
		/// </summary>
		public float Max;
	}
}